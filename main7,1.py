# Latihan konversi suhu

# Celcius ke yang lain 
print("PROGRAM KONVERSI CELCIUS KE YANG LAIN")

celcius = float(input('Masukkan Suhu ke dalam :'))
print("Suhu adalah :", celcius, "C")

# Reumur
reumur = (4/5) * celcius
print("Suhu dalam reumur :", reumur, "R")

# Farenheit
farenheit = ((9/5) * celcius) + 32
print("Suhu dalam farenheit :", farenheit, "F")

# Kelvin
kelvin = celcius + 273
print("Suhu dalam kelvin :", kelvin, "K")