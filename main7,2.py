# Konversi Farenheit Ke yang lain 

print("KONVERSI F KE YANG LAIN")

farenheit = float(input("Maksukan suhu :"))
print("Suhu adalah :",farenheit,("F"))

celcius = 5/9 *(farenheit - 32)
print("Suhu dalam celcius :", celcius,"C")

reamur = 4/9 *(farenheit - 32)
print("Suhu dalam reamur :", reamur, "R")

kelvin = (5/9 * (farenheit - 32)) + 273
print("Suhu dalam kelvin :", kelvin, "K")